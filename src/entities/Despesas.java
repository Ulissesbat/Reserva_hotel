package entities;

public class Despesas {

	private Double valor;
	
	public Despesas( Double valor) {
		
		this.valor = valor;
	}

	public Double getValor() {
		return valor;
	}

	public void setValor(Double valor) {
		this.valor = valor;
	}

	public double totalDespesas() {
		
		return valor;
	}

}


